package Entities;


public class Person {
    String firstName;
    String lastName;
    String gender;
    String phoneNumber;
    String email;

    public Person(String firstName, String lastName, String gender, String phoneNumber, String email) {
        this.firstName = firstName;
        this.lastName = lastName;
        this.gender = gender;
        this.phoneNumber = phoneNumber;
        this.email = email;
    }
}
